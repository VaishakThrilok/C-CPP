#include<stdio.h>

int main() {
    // First pattern: print 5 stars in a line
    for(int i = 0; i < 5; i++) {
        printf("*");
    }
    printf("\n");

    // Second pattern: print 15 stars in a line
    for(int i = 0; i < 15; i++) {
        printf("*");
    }
    printf("\n");

    return 0;
}
